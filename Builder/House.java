package Builder;

public class House {
	private String Door;
	private String Celling;
	private String Wall;
	public String getDoor() {
		return Door;
	}
	public void setDoor(String door) {
		Door = door;
	}
	public String getCelling() {
		return Celling;
	}
	public void setCelling(String celling) {
		Celling = celling;
	}
	public String getWall() {
		return Wall;
	}
	public void setWall(String wall) {
		Wall = wall;
	}
}
